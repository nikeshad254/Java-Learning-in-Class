package Encapsulation;

public class Encaptest {
	
	private int rollNo;
	private String name;
	
	public int getRollno() {
		return this.rollNo;
	}
	public void setRollno(int newRoll) {
		this.rollNo = newRoll;
	}
	
	public String getName() {
		return this.name;
	}
	public void setName(String newName) {
		this.name = newName;
	}
	
}
